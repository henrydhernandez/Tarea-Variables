// la tarea es investigar como funciona y que hace typeof asi como agregar la definicion.

/* DEFINICIÓN DE TYPEOF: typeof es un operador JavaScript que al ser llamado sobre una variable, 
devuelve el tipo de dato que dicha variable contiene. */

// Entero
let totalLibros = 50;

// Flotante
let pesoPromedio = 70.5;

// String
let autorNombre = "Gabriel García Márquez";

// Array
let colores = ["rojo", "verde", "azul"];

// Objeto
let vehiculo = {
  marca: "Toyota",
  modelo: "Corolla",
  colores: 'amarrillo'
};

// Booleano
let permisoObtenido = true;

console.log (typeof totalLibros);
console.log(typeof pesoPromedio);
console.log(typeof autorNombre);
console.log(typeof colores);
console.log(typeof vehiculo);
console.log(typeof permisoObtenido);

